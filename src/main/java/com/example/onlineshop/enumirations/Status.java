package com.example.onlineshop.enumirations;

/**
 * Перечисление (Enum), представляющее возможные статусы для различных сущностей,
 * таких как продукты или заказы.
 *
 * Для начинающего программиста:
 * Enum - это специальный тип данных, который позволяет определить набор
 * именованных констант. Здесь мы используем его для стандартизации статусов.
 * Например, вместо того чтобы использовать произвольные строки "новый" или "активный",
 * мы используем предопределенные константы NEW и ACTIVE. Это предотвращает опечатки
 * и делает код более надежным и читаемым.
 */
public enum Status {
    /**
     * Статус "НОВЫЙ".
     * Может использоваться для новых продуктов, новых заказов и т.д.
     */
    NEW("НОВЫЙ"),
    /**
     * Статус "Активный".
     * Может использоваться для активных продуктов, активных пользователей и т.д.
     */
    ACTIVE("Активный"),
    /**
     * Статус "Доступен".
     * Используется для продуктов, которые есть в наличии.
     */
    AVAILABLE("Доступен"),
    /**
     * Статус "Продано".
     * Используется для продуктов, которые были проданы.
     */
    SOLD("Продано"),
    /**
     * Статус "Нет в наличии".
     * Используется для продуктов, которых временно нет на складе.
     */
    OUT_OF_STOCK("Нет в наличии"),
    /**
     * Статус "В обработке".
     * Используется для заказов, которые находятся в процессе выполнения.
     */
    PROCESSING("В обработке"),
    /**
     * Статус "Отправлен".
     * Используется для заказов, которые были отправлены покупателю.
     */
    SHIPPED("Отправлен"),
    /**
     * Статус "Доставлен".
     * Используется для заказов, которые были успешно доставлены.
     */
    DELIVERED("Доставлен"),
    /**
     * Статус "Отменен".
     * Используется для заказов, которые были отменены.
     */
    CANCELLED("Отменен"),
    /**
     * Статус "Завершен".
     * Используется для заказов, которые были успешно выполнены и закрыты.
     */
    COMPLETED("Завершен");


    // Поле для хранения более читаемого названия статуса.
    private final String displayName; // Переименовано для ясности

    /**
     * Конструктор для элементов перечисления Status.
     *
     * @param displayName Строковое представление статуса, которое будет ассоциировано с константой.
     */
    Status(String displayName) {
        this.displayName = displayName;
    }

    /**
     * Возвращает строковое представление (имя) статуса.
     *
     * @return Строковое название статуса.
     */
    public String getName() {
        return displayName;
    }

    // TODO [Начинающий]: Добавьте другие необходимые статусы для продуктов (например, ARCHIVED)
    //  и для заказов.
    //  Убедитесь, что эти статусы соответствуют вашей бизнес-логике.
    // TODO [Начинающий]: Рассмотрите возможность добавления статического метода `fromDisplayName(String name)`
    //  для преобразования строкового имени обратно в Enum, если это потребуется.
}
